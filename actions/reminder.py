# [FIX-1] Cached OS detection
_os_cache: str | None = None


def _base_dir() -> Path:
    if getattr(sys, "frozen", False):
        return Path(sys.executable).parent
    return Path(__file__).resolve().parent.parent


def _get_os() -> str:
    global _os_cache
    if _os_cache is not None:
        return _os_cache
    try:
        cfg = json.loads(
            (_base_dir() / "config" / "api_keys.json").read_text(encoding="utf-8")
        )
        _os_cache = cfg.get("os_system", "").lower()
        if not _os_cache:
            import platform
            system = platform.system().lower()
            _os_cache = {"darwin": "mac", "windows": "windows", "linux": "linux"}.get(system, "windows")
    except Exception:
        import platform
        system = platform.system().lower()
        _os_cache = {"darwin": "mac", "windows": "windows", "linux": "linux"}.get(system, "windows")
    return _os_cache


def _scripts_dir() -> Path:
    d = Path.home() / ".friday" / "reminders"
    d.mkdir(parents=True, exist_ok=True)
    return d


# [FIX-3] Smarter sanitization — preserve message meaning
def _sanitise(text: str, max_len: int = 200) -> str:
    # Remove control characters but keep normal punctuation
    text = re.sub(r"[\x00-\x08\x0b\x0c\x0e-\x1f]", "", text)
    text = text.replace("\n", " ").replace("\r", "").strip()
    return text[:max_len]


